console.log(" ----- Area Of Shapes JS Arithmetic Challenge ----- \n\n")

// Constant Variables:
const pi = 3.1419;
const half = 0.5;

// Variables for measurements
var rectangleLength = 10;
var rectangleBreadth = 17;
var circleRadius = 25;
var triangleBase = 20;
var triangleHeight = 9;



// Rectangle area calculation
const areaRectangle = rectangleLength * rectangleBreadth;

// Append your rectangle answer:
console.log("The area of the given rectangle is : " + areaRectangle);


console.log("\n");


// circle area calculation
const areaCircle = (pi * circleRadius) * (pi * circleRadius);

// Append your circle answer:
console.log("The area of the given circle is : " + areaCircle);


console.log("\n");


// triangle area calculation
const areaTriangle = (half * triangleBase) * triangleHeight;

// Append your triangle answer:
console.log("The area of the given triangle is : " + areaTriangle);